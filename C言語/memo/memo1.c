/*
キャスト演算子
式のデータ型を()の中で指定した型に変換します。
具体的には次のように記述します。

double a = 10.5;
int b = (int)a;

上記の例では変数「a」の値を「int」型に変換したものを
左辺の変数「b」に格納します。
*/


