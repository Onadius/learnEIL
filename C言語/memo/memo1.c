/*
静的変数について
static

関数の中で局所変数を宣言する際に記憶クラスとしてstaticを与えると、
その関数が終了しても消滅せず、値を残すことができます。
staticな局所変数は定義時に0で初期化されます。
普通の局所変数のように関数がコールされた時のみ存在するのではなく、永久に存在します。
しかし、外部変数とは異なり、他の関数からは利用できません。
*/


